WITH AVAIL AS(
    SELECT c.CAR_ID, c.CAR_TYPE, c.DAILY_FEE, '30일 이상' as DURATION_TYPE
    FROM CAR_RENTAL_COMPANY_CAR c
    JOIN (SELECT CAR_ID, SUM(CASE 
            WHEN END_DATE < '2022-11-01' OR START_DATE > '2022-11-30' THEN 0
            ELSE 1 END) AS TEMP
        FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
        GROUP BY CAR_ID) AS t
    ON c.CAR_ID = t.CAR_ID
    WHERE t.TEMP = 0
    AND c.CAR_TYPE IN ('세단', 'SUV')
)

SELECT a.CAR_ID, a.CAR_TYPE, 
    ROUND(a.DAILY_FEE * ((100 - p.DISCOUNT_RATE) / 100), 0) * 30 AS FEE
FROM CAR_RENTAL_COMPANY_DISCOUNT_PLAN p
JOIN AVAIL a ON p.CAR_TYPE = a.CAR_TYPE
AND p.DURATION_TYPE = a.DURATION_TYPE
WHERE ROUND(a.DAILY_FEE * ((100 - p.DISCOUNT_RATE) / 100), 0) * 30
   BETWEEN 500000 AND 2000000
ORDER BY FEE DESC, a.CAR_TYPE, a.CAR_ID DESC;
